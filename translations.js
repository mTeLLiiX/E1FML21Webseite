// Funktion zum Ändern der Sprache
function changeLanguage(lang) {
    localStorage.setItem('preferredLanguage', lang);
    updateLanguage();
}

// Funktion zum Aktualisieren der Sprache
function updateLanguage() {
    const lang = localStorage.getItem('preferredLanguage') || 'de';
    const currentTranslations = translations[lang];
    
    // Aktualisiere alle Elemente mit data-translate Attribut
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.dataset.translate;
        if (currentTranslations[key]) {
            element.textContent = currentTranslations[key];
        }
    });
    
    // Aktualisiere die aktive Sprache in den Buttons
    document.querySelectorAll('.language-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });
}

// Initialisiere die Sprache beim Laden der Seite
document.addEventListener('DOMContentLoaded', () => {
    const preferredLang = localStorage.getItem('preferredLanguage') || 'de';
    updateLanguage();
}); 